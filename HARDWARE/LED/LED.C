#include "led.h"
//初始化 PC13 为输出口.并使能PORTC口的时钟
void LED_Init(void)
{
RCC->APB2ENR|=1<<4; //使能 PORTC 时钟  
GPIOC->CRH&=0XFF0FFFFF;
GPIOC->CRH|=0X00300000;//PC13 推挽输出  0X3 表示推挽输出模式
GPIOC->ODR|=1<<13; //PC13 输出高
}

